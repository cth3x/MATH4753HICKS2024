#' Square a Number
#'
#' This function computes the square of a given numeric input.
#'
#' @param x Numeric. A value or vector of values to be squared.
#'
#' @return Numeric. The square of the input value(s).
#'
#' @export
#'
#' @examples
#' # Square a single number
#' mysquare(4) # Returns 16
mysquare <- function(x) {
  x^2
}
